class Solution
{
    public static int fun(Node head)
    {
        if(head == null)
        {
            return 1;
        }
        int x = head.data+fun(head.next);
        head.data = x%10;
        return x/10;
    }
    public static Node addOne(Node head) 
    { 
        //code here.
        int x = fun(head);
        if(x != 0)
        {
            Node temp = head;
            head = new Node(x);
            head.next = temp;
        }
        return head;
    }
}
